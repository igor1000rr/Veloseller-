/**
 * CSV-экранирование одного значения — единый источник для всех экспортов.
 *
 * Делает две вещи:
 *  1. Структурная безопасность CSV: значения с разделителем / кавычкой /
 *     переводом строки заворачиваются в кавычки (внутренние кавычки удваиваются).
 *  2. Anti-formula-injection: ведущие = + - @ (а также TAB/CR) заставляют Excel,
 *     LibreOffice и Google Sheets интерпретировать ячейку как ФОРМУЛУ при открытии
 *     файла. sku/название/заметки задаёт арендатор, а экспорт открывает сотрудник
 *     или админ — то есть это путь к выполнению формулы (=HYPERLINK/=cmd|…/DDE) на
 *     чужой машине. Нейтрализуем ведущим апострофом (стандартный приём OWASP).
 *
 * Раньше каждый экспорт имел свою копию escape() БЕЗ п.2 — отсюда дублирование
 * и пропуск защиты. Теперь правка одна и переиспользуется.
 */
export function csvEscape(value: unknown, sep: string = ","): string {
  if (value == null) return "";
  let s = String(value);
  // Formula injection: нейтрализуем опасный ведущий символ апострофом.
  if (/^[=+\-@\t\r]/.test(s)) s = "'" + s;
  if (s.includes(sep) || s.includes('"') || s.includes("\n")) {
    return `"${s.replace(/"/g, '""')}"`;
  }
  return s;
}
