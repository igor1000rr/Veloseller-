"""Календарь федеральных праздников РФ для anomaly-расчёта.

Праздничные дни исключаются из расчёта медианы 30-дневных дельт и из классификации
anomaly_like, потому что в эти дни продажи ведут себя аномально (пики пред-раздачи
и провалы в сами праздники), и их неверно принимать за эталон или выброс.

Общие выходные РФ по ТК (ст. 112) без учёта переносов Правительства
(переносы меняются каждый год — Александр добавит их вручную позже).
Новогодние: 1-8 января. Другие: 23 фев, 8 мар, 1 мая, 9 мая, 12 июн, 4 ноя.
"""
from __future__ import annotations
from datetime import date
from functools import lru_cache


@lru_cache(maxsize=32)
def russian_federal_holidays(year: int) -> frozenset[date]:
    """Возвращает фиксированные федеральные праздники РФ для указанного года.

    Без учёта переносов выходных (когда 8 марта падает на воскресенье,
    понедельник объявляют выходным). Добавятся вручную когда Александр пришлёт список.
    """
    days = set()
    # Новогодние каникулы: 1-8 января
    for d in range(1, 9):
        days.add(date(year, 1, d))
    days.add(date(year, 2, 23))   # День защитника Отечества
    days.add(date(year, 3, 8))    # Международный женский день
    days.add(date(year, 5, 1))    # Праздник Весны и Труда
    days.add(date(year, 5, 9))    # День Победы
    days.add(date(year, 6, 12))   # День России
    days.add(date(year, 11, 4))   # День народного единства
    return frozenset(days)


def is_holiday(d: date) -> bool:
    """True если дата — федеральный праздник РФ."""
    return d in russian_federal_holidays(d.year)
