-- Ставка налога на уровне кабинета (продавца). Один процент на весь кабинет
-- (налоговый режим: УСН/ОСН и т.п.). Используется как дефолт в блоке
-- Юнит-экономика по всем товарам кабинета. Задаётся в массовой загрузке
-- себестоимости («Применить ко всем товарам кабинета»). Идемпотентно.
alter table public.sellers add column if not exists tax_rate numeric;

comment on column public.sellers.tax_rate is
  'Ставка налога продавца в процентах (например 6 для УСН 6%). Дефолт юнит-экономики по всем товарам кабинета. NULL — не задана.';
