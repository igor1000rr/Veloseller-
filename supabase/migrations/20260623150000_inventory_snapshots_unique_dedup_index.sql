-- ============================================================================
-- Backstop дедупа снапшотов на уровне БД (audit P2).
--
-- ПРОБЛЕМА: корректность дедупа снапшотов держалась ТОЛЬКО на приложении —
-- 20-часовое окно в ingest_persist._persist_snapshots + атомарный sync-лок
-- (data_connections.status='syncing'). Уникального ограничения в БД не было,
-- поэтому любой путь, теряющий лок или дважды вызывающий persist с теми же
-- данными (тот же snapshot_time), мог создать точные дубли, которые движок
-- дельт прочитал бы как ложное движение остатка.
--
-- РЕШЕНИЕ: уникальный индекс (product_id, snapshot_time) как defense-in-depth.
--   • В пределах одного синка все строки имеют ОДИН snapshot_time (now) и РАЗНЫЕ
--     product_id → нарушений в норме не бывает, легитимные внутридневные записи
--     с другим временем (стало известно новое значение) НЕ блокируются.
--   • Гонку двух синков с РАЗНЫМ now индекс не ловит (там заслон — sync-лок и
--     20h-дедуп); он закрывает ИМЕННО точные дубли (ретрай/двойной вызов).
--   • Поведение fail-loud (insert упадёт на настоящем дубле) согласуется с общей
--     философией воркера «лучше упасть, чем записать фантом».
--
-- Применяется ВРУЧНУЮ на self-hosted prod (нет runner'а). Идемпотентна.
-- ============================================================================

-- 1. Снести существующие ТОЧНЫЕ дубли (если есть), оставив строку с минимальным
--    ctid в каждой группе (product_id, snapshot_time). В норме дублей нет.
DELETE FROM public.inventory_snapshots a
USING public.inventory_snapshots b
WHERE a.product_id = b.product_id
  AND a.snapshot_time = b.snapshot_time
  AND a.ctid > b.ctid;

-- 2. Уникальный индекс-бэкстоп.
CREATE UNIQUE INDEX IF NOT EXISTS uq_snapshots_product_time
  ON public.inventory_snapshots (product_id, snapshot_time);

-- 3. Старый неуникальный idx_snapshots_product_time (product_id, snapshot_time DESC)
--    стал избыточным: btree уникального индекса обслуживает и ASC-, и DESC-сканы
--    по тем же колонкам (PG умеет сканировать в обе стороны). Убираем дубль-индекс,
--    чтобы не платить за две почти одинаковые структуры на каждую вставку.
DROP INDEX IF EXISTS public.idx_snapshots_product_time;
